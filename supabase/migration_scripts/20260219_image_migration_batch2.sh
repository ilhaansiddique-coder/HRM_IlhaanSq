#!/bin/bash
# Migrate images from SECOND old Supabase (smopyfuaijaklmtpwgws) to self-hosted
OLD_SUPABASE_URL="https://smopyfuaijaklmtpwgws.supabase.co"
NEW_SUPABASE_URL="https://supabase.inventra.site"
NEW_SERVICE_ROLE_KEY="eyJ0eXAiOiJKV1QiLCJhbGciOiJIUzI1NiJ9.eyJpc3MiOiJzdXBhYmFzZSIsImlhdCI6MTc3MTEyNDU4MCwiZXhwIjo0OTI2Nzk4MTgwLCJyb2xlIjoic2VydmljZV9yb2xlIn0.Vg8sx1ePiAxm2o9ipgbW5ng_5jq0dCzlfx-LjV2VA7w"
BUCKET="product-images"

TEMP_DIR="/tmp/supabase-image-migration-2"
mkdir -p "$TEMP_DIR"

SUCCESS=0
FAIL=0
SKIP=0
TOTAL=0

echo "=== Supabase Image Migration (Batch 2) ==="
echo "From: $OLD_SUPABASE_URL"
echo "To:   $NEW_SUPABASE_URL"
echo ""

while IFS= read -r filename; do
    TOTAL=$((TOTAL + 1))

    echo "[$TOTAL] Downloading: $filename"
    HTTP_CODE=$(curl -s -o "$TEMP_DIR/$filename" -w "%{http_code}" \
        "$OLD_SUPABASE_URL/storage/v1/object/public/$BUCKET/$filename")

    if [ "$HTTP_CODE" != "200" ]; then
        echo "  SKIP - Download failed (HTTP $HTTP_CODE)"
        SKIP=$((SKIP + 1))
        rm -f "$TEMP_DIR/$filename"
        continue
    fi

    FILE_SIZE=$(stat -c%s "$TEMP_DIR/$filename" 2>/dev/null || stat -f%z "$TEMP_DIR/$filename" 2>/dev/null)
    echo "  Downloaded ($FILE_SIZE bytes)"

    CONTENT_TYPE="image/jpeg"
    case "$filename" in
        *.png) CONTENT_TYPE="image/png" ;;
        *.webp) CONTENT_TYPE="image/webp" ;;
        *.gif) CONTENT_TYPE="image/gif" ;;
    esac

    echo "  Uploading to self-hosted..."
    UPLOAD_RESPONSE=$(curl -s -w "\n%{http_code}" \
        -X POST "$NEW_SUPABASE_URL/storage/v1/object/$BUCKET/$filename" \
        -H "Authorization: Bearer $NEW_SERVICE_ROLE_KEY" \
        -H "Content-Type: $CONTENT_TYPE" \
        -H "x-upsert: true" \
        --data-binary "@$TEMP_DIR/$filename")

    UPLOAD_HTTP_CODE=$(echo "$UPLOAD_RESPONSE" | tail -1)
    UPLOAD_BODY=$(echo "$UPLOAD_RESPONSE" | head -n -1)

    if [ "$UPLOAD_HTTP_CODE" = "200" ] || [ "$UPLOAD_HTTP_CODE" = "201" ]; then
        echo "  OK - Uploaded successfully"
        SUCCESS=$((SUCCESS + 1))
    else
        echo "  FAIL - Upload failed (HTTP $UPLOAD_HTTP_CODE): $UPLOAD_BODY"
        FAIL=$((FAIL + 1))
    fi

    rm -f "$TEMP_DIR/$filename"

done <<'FILELIST'
